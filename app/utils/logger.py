import logging

import colorlog


def setup_logger():
    log = logging.getLogger('jamify-ai')
    log.setLevel(logging.INFO)
    handler = logging.StreamHandler()
    log_format = '%(log_color)s%(asctime)s-%(name)s-%(funcName)s-%(levelname)s: %(message)s'
    log_colors = {
        'DEBUG': 'cyan',
        'INFO': 'green',
        'WARNING': 'yellow',
        'ERROR': 'red',
        'CRITICAL': 'magenta',
    }
    formatter = colorlog.ColoredFormatter(log_format, log_colors=log_colors)
    handler.setFormatter(formatter)
    log.addHandler(handler)
    return log


logger = setup_logger()
