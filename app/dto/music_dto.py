from marshmallow import Schema, fields


class MusicDTO(Schema):
    id = fields.Int(required=False)
    isrc = fields.String(required=False)
    author = fields.String(required=False)
    title = fields.String(required=True)
    imgurl = fields.String(required=False)
    tempo = fields.String(required=False)
    energy = fields.String(required=False)

    def __init__(self, id=None, isrc=None, author=None, title=None, imgurl=None, tempo=None, energy=None):
        super().__init__()
        self.id = id
        self.isrc = isrc
        self.author = author
        self.title = title
        self.imgurl = imgurl
        self.tempo = tempo
        self.energy = energy
